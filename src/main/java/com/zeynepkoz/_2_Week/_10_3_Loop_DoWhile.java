package com.zeynepkoz._2_Week;

public class _10_3_Loop_DoWhile {
    public static void main(String[] args) {

        // do-while şart sağlansın ya da sağlanmasın bir kere çalışır.
        int i = 1;
        do{
            System.out.print(i + " - ");
            i++;
        }while (i <= 10);

    }
}
