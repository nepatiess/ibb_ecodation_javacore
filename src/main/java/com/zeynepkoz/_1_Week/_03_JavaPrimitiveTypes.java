package com.zeynepkoz._1_Week;

public class _03_JavaPrimitiveTypes {

    public static void main(String[] args) {

        // Primitive type 8 tanedir, null değer alamayız.

        // tam sayılar (4)

        byte b1 = 127;
        System.out.println("bytes: " + b1);

        short s1 = 16516;
        System.out.println("short: " + s1);

        int i1 = 254465221;
        System.out.println("int: " + i1);

        long l1 = 9845263567787252L;
        System.out.println("long: " + l1);

        // virgüllü sayılar (floating point)
        float f2 = 3.14f;
        System.out.println("float: " + f2);

        double d2 = 3.14;
        System.out.println("double: " + d2);

        boolean b2 = true;
        System.out.println("boolean: " + b2);

        char c2 = 'Z';
        System.out.println("char: " + c2);


    }
}
